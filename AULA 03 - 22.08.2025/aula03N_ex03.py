
'''
Ler dois valores numéricos e escrever a soma destes.
'''

vA = float( input("Digite o primeiro número: ") )
vB = float( input("Digite o segundo número: ") )


print(vA, "+", vB, "=", vA + vB)
print(f"{vA} + {vB} = {vA + vB}  \n{vA} - {vB} = {vA - vB}")


print(vA - vB)
print(vA * vB)
print(vA / vB)
print(vA // vB)
print(vA % vB)

